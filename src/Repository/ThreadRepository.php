class ThreadRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Thread::class);
    }

    public function findCurrentThreadWithRelations(string $id): Thread
    {
        return $this->createQueryBuilder('thread')
            ->select('thread','sub_category', 'category', 'posts')
            ->leftJoin('thread.subCategory','sub_category')
            ->leftJoin('sub_category.category','category')
            ->leftJoin('thread.posts','posts')
            ->where('thread.id = :id')
            ->setParameter(':id', $id)
            ->getQuery()
            ->getOneOrNullResult();
    }

